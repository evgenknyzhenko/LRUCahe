public class Entry {
    private int key;
    private int valuee;

    public Entry(int key, int valuee) {
        this.key = key;
        this.valuee = valuee;
    }


    public int getKey() {
        return key;
    }

    public int getValuee() {
        return valuee;
    }

    public void setValuee(int valuee) {
        this.valuee = valuee;
    }

}
